from difference import difference
from summation import summation
from division import div
from multiplication import mult

class Calculator:

    def summation(a, b):
        return summation(a, b)

    def difference(a, b):
        return difference(a, b)
    
    def multiplication(a, b):
        return mult(a, b)
    
    def dividion(a, b):
        return div(a, b)
    